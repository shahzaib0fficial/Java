public class c7EscapeSequences {
    public static void main(String args[]){
        // By using Escape Sequences we can modify our output and add those character which is not allowed directly to print directly
        System.out.println("After \nFurther text is on next line");
        System.out.println("For\tis used to give tab spaces");
        System.out.println("To print backslash \\  is used");
        System.out.println("To print double quotation \" is used");
    }
}
