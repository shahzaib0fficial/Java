public class c15ComparisionOperators {
    public static void main(String[] args){
        // These operators returns true or false according to the the condition
        int a = 5;
        int b=10;
        System.out.println("Equals equals to : "+(a==b)); // return true if both are equal otherwise return false
        System.out.println("Not equals to : "+(a!=b)); // return true if both are not equal otherwise return false
        System.out.println("Smaller than : "+(a<b)); // return true if a is smaller than b otherwise return false
        System.out.println("Smaller than or equals to : "+(a<=b)); // return true if a is smaller than or equals to b otherwise return false
        System.out.println("Greater than : "+(a>b)); // return true if a is greater than b otherwise return false
        System.out.println("Greater than or equals to : "+(a>=b)); // return true if a is smaller than or equals to b otherwise return false
    }
}